#include <iostream>
#include <vector>
using namespace std;
int main(){
  int size_of_array;
  vector<int> list;
  cin >> size_of_array;
  while(size_of_array != 0){
    int data;
    cin >> data;
    list.push_back(data);
    --size_of_array;
  }
  for(int i = list.size() - 1; i >= 0; --i){
    cout << list.at(i) << " ";
  }
  return 0;
}
